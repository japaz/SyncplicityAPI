package org.japj;

public class GlobalFileData {
	String Hash;
    Long Length;
    Boolean Stored;
    
	public String getHash() {
		return Hash;
	}
	public void setHash(String hash) {
		Hash = hash;
	}
	public Long getLength() {
		return Length;
	}
	public void setLength(Long length) {
		Length = length;
	}
	public Boolean getStored() {
		return Stored;
	}
	public void setStored(Boolean stored) {
		Stored = stored;
	}
    
    
}
